package ntua.multimedia.code;

/**
 * The Enum Status.
 */
public enum Status {

	/** The playing status. When a song is playing. */
	PLAYING,
	
	/** The stopped status. When a song has stopped playing. */
	STOPPED,
	
	/** The paused status. When a song has paused. */
	PAUSED,
	
	/** The null status. When no song has been selected or a song has just been selected  */
	NULL;
}
